package spend.util;

import java.util.ArrayList;
import java.util.List;

public class LineChartsData{
	public String name;
	public List data;
	public LineChartsData(){
		name=new String();
		data=new ArrayList();
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public List getData() {
		return data;
	}
	public void setData(List data) {
		this.data = data;
	}
	
}